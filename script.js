// Download Report
// ==========================================

function downloadReport(){

    const report =
    document.getElementById("report").textContent;

    if(report.trim()===""){

        alert("Please Generate Report First.");

        return;

    }

    const blob = new Blob([report],{
        type:"text/plain"
    });

    const link = document.createElement("a");

    link.href = URL.createObjectURL(blob);

    link.download =
    "Running_Report.txt";

    link.click();

}


