// Print Report
// ==========================================

function printReport(){

    const report =
    document.getElementById("report").textContent;

    if(report.trim()===""){

        alert("Please Generate Report First.");

        return;

    }

    const printWindow =
    window.open("","_blank");

    printWindow.document.write(

    "<pre style='font-size:14px;font-family:Consolas;'>"

    + report +

    "</pre>"

    );

    printWindow.document.close();

    printWindow.print();

}// ==========================================
