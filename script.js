// Auto Report Number

function getReportNumber(){

    const now = new Date();

    return "RGP-" +
    now.getFullYear() +
    String(now.getMonth()+1).padStart(2,"0") +
    String(now.getDate()).padStart(2,"0") +
    "-" +
    now.getHours() +
    now.getMinutes() +
    now.getSeconds();

}


// Current Date & Time

function getCurrentDateTime(){

    return new Date().toLocaleString("en-IN");

}


// Validate Required Fields

function validateForm(){

    let requiredFields=[

        "trainNo",
        "lpName",
        "tmName",
        "fromStation",
        "toStation"

    ];

    for(let i=0;i<requiredFields.length;i++){

        let value=document.getElementById(requiredFields[i]).value.trim();

        if(value===""){

            alert("Please Fill All Required Fields.");

            document.getElementById(requiredFields[i]).focus();

            return false;

        }

    }

    return true;

}


// Generate Button Validation

const oldGenerateReport = generateReport;

generateReport = function(){

    if(!validateForm()){

        return;

    }

    oldGenerateReport();

    let report=document.getElementById("report").textContent;

    report += "\n\n";

    report += "----------------------------------------\n";

    report += "Report No : " + getReportNumber() + "\n";

    report += "Generated : " + getCurrentDateTime() + "\n";

    report += "Software  : RailGuardPro v2.0\n";

    report += "Developed By : Munesh Kumar Meena\n";

    report += "----------------------------------------";

    document.getElementById("report").textContent=report;

};


