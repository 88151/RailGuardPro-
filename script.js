// Save Report

function saveReport(){

    localStorage.setItem(
        "RailGuardPro_Report",
        document.getElementById("report").textContent
    );

}


// Load Last Report

function loadReport(){

    const lastReport =
    localStorage.getItem("RailGuardPro_Report");

    if(lastReport){

        document.getElementById("report").textContent =
        lastReport;

    }

}


// Auto Save after Generate

const previousGenerate = generateReport;

generateReport = function(){

    previousGenerate();

    saveReport();

};


// Clear Local Storage

function clearSavedReport(){

    localStorage.removeItem("RailGuardPro_Report");

}


// Update Clear Button

const previousClear = clearForm;

clearForm = function(){

    previousClear();

    clearSavedReport();

};


// Initialize

window.addEventListener("load",function(){

    loadReport();

});


